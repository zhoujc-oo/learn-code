public class xxxValue {
    public static void main(String[] args) {
        Integer x = 5;

        //返回byte原生数据类型
        System.out.println(x.byteValue());
        //返回double原生数据类型
        System.out.println(x.doubleValue());
        //返回long原生数据类型
        System.out.println(x.longValue());
    }
}
